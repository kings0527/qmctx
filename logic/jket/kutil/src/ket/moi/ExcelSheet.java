
package ket.moi;

public interface ExcelSheet
{
	boolean isNotEmpty(int r, int c);
	String getStringValue(int r, int c) throws ReaderException;
	int getIntValue(int r, int c) throws ReaderException;
	short getShortValue(int r, int c) throws ReaderException;
	byte getByteValue(int r, int c) throws ReaderException;
	long getLongValue(int r, int c) throws ReaderException;
	float getFloatValue(int r, int c) throws ReaderException;
	double getDoubleValue(int r, int c) throws ReaderException;
}
